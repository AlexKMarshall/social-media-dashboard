type SocialFollowersCardProps = {
  name: string
  socialLink: JSX.Element
  logo?: JSX.Element
  value: number
  metric?: string
  changePercent: number
  period?: string
}
function SocialFollowersCard({
  name: brand,
  socialLink,
  logo,
  value,
  metric = "followers",
  changePercent,
  period = "today",
}: SocialFollowersCardProps): JSX.Element {
  const headingId = useId()

  const changeDirection = changePercent >= 0 ? "up" : "down"
  return (
    <article aria-labelledby={headingId}>
      <header>
        <h2 id={headingId}>{brand}</h2>
        <p>{socialLink}</p>
      </header>
      <p>
        <span>{value}</span> <span>{metric}</span>
      </p>
      <footer>
        <span>{changeDirection}</span> {changePercent} {period}
      </footer>
    </article>
  )
}

type MetricCardProps = {
  metric: string
  brand: string
  value: number
  change: number
}
function MetricCard({
  metric,
  brand,
  value,
  change,
}: MetricCardProps): JSX.Element {
  const headingId = useId()
  const changeDirection = change >= 0 ? "up" : "down"

  return (
    <article aria-labelledby={headingId}>
      <h3 id={headingId}>
        <span>{metric}</span>
        <span>{brand}</span>
      </h3>
      <p>{value}</p>
      <p>
        <span>{changeDirection}</span>
        <span>{change}%</span>
      </p>
    </article>
  )
}
